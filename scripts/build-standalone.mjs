import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(__dirname, "..");

const moduleSpecs = [
  { id: "data", file: "scheduling/js/data.js" },
  { id: "algorithms", file: "scheduling/js/algorithms.js" },
  { id: "i18n", file: "scheduling/js/i18n.js" },
  { id: "render", file: "scheduling/js/render.js" },
  { id: "app", file: "scheduling/js/app.js", entry: true },
];

const importPattern = /^import\s*\{([\s\S]*?)\}\s*from\s*"(.+?)";\s*$/gm;

function normalizeImportList(specifierBlock) {
  return specifierBlock
    .split(",")
    .map((part) => part.trim())
    .filter(Boolean)
    .map((part) => {
      const [imported, local] = part.split(/\s+as\s+/).map((value) => value.trim());
      return {
        imported,
        local: local ?? imported,
      };
    });
}

function moduleIdFromImport(importPath) {
  const filename = path.basename(importPath, path.extname(importPath));
  return filename;
}

function transformModule(source) {
  const imports = [];
  let transformed = source.replace(importPattern, (_, specifierBlock, importPath) => {
    imports.push({
      moduleId: moduleIdFromImport(importPath),
      bindings: normalizeImportList(specifierBlock),
    });
    return "";
  });

  const exports = [];
  transformed = transformed.replace(/export function (\w+)/g, (_, name) => {
    exports.push(name);
    return `function ${name}`;
  });
  transformed = transformed.replace(/export const (\w+)/g, (_, name) => {
    exports.push(name);
    return `const ${name}`;
  });

  return { imports, exports, transformed: transformed.trim() };
}

function buildModuleWrapper(spec, source) {
  const { imports, exports, transformed } = transformModule(source);
  const importLines = imports.flatMap(({ moduleId, bindings }) => {
    const named = bindings.map(({ imported, local }) => (imported === local ? imported : `${imported}: ${local}`)).join(", ");
    return [`const { ${named} } = __module_${moduleId};`];
  });

  if (spec.entry) {
    return [
      "  (() => {",
      ...importLines.map((line) => `    ${line}`),
      transformed
        .split("\n")
        .map((line) => `    ${line}`)
        .join("\n"),
      "  })();",
    ].join("\n");
  }

  return [
    `  const __module_${spec.id} = (() => {`,
    ...importLines.map((line) => `    ${line}`),
    transformed
      .split("\n")
      .map((line) => `    ${line}`)
      .join("\n"),
    `    return { ${exports.join(", ")} };`,
    "  })();",
  ].join("\n");
}

async function main() {
  const sections = [];
  for (const spec of moduleSpecs) {
    const sourcePath = path.join(rootDir, spec.file);
    const source = await fs.readFile(sourcePath, "utf8");
    sections.push(buildModuleWrapper(spec, source));
  }

  const output = [
    "/*",
    " * Generated by scripts/build-standalone.mjs.",
    " * Do not edit this file directly.",
    " */",
    "(() => {",
    '  "use strict";',
    ...sections,
    "})();",
    "",
  ].join("\n");

  const outputPath = path.join(rootDir, "scheduling/js/app.standalone.js");
  await fs.writeFile(outputPath, output, "utf8");
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
